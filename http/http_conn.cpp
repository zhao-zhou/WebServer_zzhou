//搭建user的map，便于用户登陆查询
void http_conn::initmysql_result(connection_pool *connPool)
{
    //先从连接池中取一个连接
    MYSQL *mysql = NULL;
    connectionRAII mysqlcon(&mysql, connPool);

    //在user表中检索username，passwd数据，浏览器端输入
    mysql_query(mysql, "SELECT username,passwd FROM user");

    //从表中检索完整的结果集
    MYSQL_RES *result = mysql_store_result(mysql);

    //从结果集中获取下一行，将对应的用户名和密码，存入map中
    while (MYSQL_ROW row = mysql_fetch_row(result))
    {
        string tmp1(row[0]);
        string tmp2(row[1]);
        users[tmp1] = tmp2;
    }

    mysql_query(mysql, "SELECT username,txtnum FROM user");

    //从表中检索完整的结果集
    result = mysql_store_result(mysql);

    //从结果集中获取下一行，将对应的用户名和用户文件数目，存入map中
    while (MYSQL_ROW row = mysql_fetch_row(result))
    {
        string tmp3(row[0]);
        int tmp4 = int(*row[1] - '0');
        usersTxt[tmp3] = tmp4;
    }
}

//关闭连接，关闭一个连接，客户总量减一
void http_conn::close_conn(bool real_close)
{
    if (real_close && (m_sockfd != -1))
    {
        epoll_tool::removefd(m_epollfd, m_sockfd);
        m_sockfd = -1;
        m_user_count--;
    }
}

//初始化连接,外部调用初始化套接字地址
void http_conn::init(int sockfd, const sockaddr_in &addr, char *root, int TRIGMode,
                     string user, string passwd, string sqlname)
{
    m_sockfd = sockfd;
    m_address = addr;

    epoll_tool::addfd(m_epollfd, sockfd, true, m_TRIGMode);
    m_user_count++;

    //当浏览器出现连接重置时，可能是网站根目录出错或http响应格式出错或者访问的文件中内容完全为空
    doc_root = root;
    m_TRIGMode = TRIGMode;

    strcpy(sql_user, user.c_str());
    strcpy(sql_passwd, passwd.c_str());
    strcpy(sql_name, sqlname.c_str());

    init();
}

